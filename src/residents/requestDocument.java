    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed

        DocumentItem selected =
            (DocumentItem) documentComboBox.getSelectedItem();

        if (selected == null) {
            JOptionPane.showMessageDialog(this,
                "Please select a document.",
                "Validation Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        String purpose = jTextArea1.getText().trim();

        if (purpose.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Purpose cannot be empty.",
                "Validation Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            connectDB db = new connectDB();
            Connection conn = db.getConnection();

            // üîé Get resident_id from users table
            String getResidentSql =
                "SELECT resident_id FROM users WHERE user_id = ?";
            PreparedStatement pst1 =
                conn.prepareStatement(getResidentSql);
            pst1.setInt(1,
                Config.User.getInstance().getUserId());

            ResultSet rs = pst1.executeQuery();

            Integer residentId = null;
            if (rs.next()) {
                residentId = (Integer) rs.getObject("resident_id");
            }

            rs.close();
            pst1.close();

            if (residentId == null) {
                JOptionPane.showMessageDialog(this,
                    "Resident profile not linked.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
                return;
            }

            // üìù Insert request
            String insertSql =
                "INSERT INTO requests (resident_id, document_id, purpose) " +
                "VALUES (?, ?, ?)";

            PreparedStatement pst2 =
                conn.prepareStatement(insertSql);

            pst2.setInt(1, residentId);
            pst2.setInt(2, selected.getId());
            pst2.setString(3, purpose);

            pst2.executeUpdate();
            pst2.close();
            conn.close();

            JOptionPane.showMessageDialog(this,
                "Request submitted successfully!",
                "Success",
                JOptionPane.INFORMATION_MESSAGE);

            // Clear form
            documentComboBox.setSelectedIndex(-1);
            jTextArea1.setText("");
            name.setText("Estimated Fee:");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(),
                "Database Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_submitActionPerformed
    private void loadDocuments() {

    try {
        connectDB db = new connectDB();
        Connection conn = db.getConnection();

       String sql = "SELECT document_id, document_name, fee FROM documents";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        documentComboBox.removeAllItems();

        documentComboBox.addItem(null); // Optional first blank

        while (rs.next()) {
            int id = rs.getInt("document_id");
            String name = rs.getString("document_name");
            double fee = rs.getDouble("fee");

            documentComboBox.addItem(new DocumentItem(id, name, fee));
        }

        rs.close();
        pst.close();
        conn.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}
    

