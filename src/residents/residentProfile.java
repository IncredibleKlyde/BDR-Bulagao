    private void loadResidentProfile() {

        try {
            config.connectDB db = new config.connectDB();
            java.sql.Connection conn = db.getConnection();

            String sql =
                "SELECT r.* FROM residents r " +
                "JOIN users u ON r.resident_id = u.resident_id " +
                "WHERE u.user_id = ?";

            java.sql.PreparedStatement pst =
                conn.prepareStatement(sql);

            pst.setInt(1, Config.User.getInstance().getUserId());

            java.sql.ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                firstName.setText(rs.getString("first_name"));
                middleName.setText(rs.getString("middle_name"));
                lastName.setText(rs.getString("last_name"));
                birthdate.setText(rs.getString("birthdate"));
                contactNumber.setText(rs.getString("contact_number"));
                address.setText(rs.getString("address"));
                purok.setText(rs.getString("purok"));

                genderCombo.setSelectedItem(rs.getString("gender"));
                civilStatus.setSelectedItem(rs.getString("civil_status"));

                submit.setText("Update Profile"); // change button label
            }

            rs.close();
            pst.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Error loading profile: " + e.getMessage(),
                "Database Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }
