private void checkProfileStatus() {

    try {
        config.connectDB db = new config.connectDB();
        java.sql.Connection conn = db.getConnection();

        String sql = "SELECT resident_id FROM users WHERE user_id = ?";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setInt(1, Config.User.getInstance().getUserId());

        java.sql.ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            Integer residentId = (Integer) rs.getObject("resident_id");

            if (residentId == null) {
                JOptionPane.showMessageDialog(this,
                    "Please complete your resident profile first.",
                    "Profile Required",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        }

        rs.close();
        pst.close();
        conn.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,
            "Error checking profile: " + e.getMessage());
    }
}
