    private void requestPanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_requestPanelMouseClicked
        
        try {
            config.connectDB db = new config.connectDB();
            java.sql.Connection conn = db.getConnection();

            String sql = "SELECT resident_id FROM users WHERE user_id = ?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, Config.User.getInstance().getUserId());

            java.sql.ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                Integer residentId = (Integer) rs.getObject("resident_id");

                if (residentId == null) {
                    JOptionPane.showMessageDialog(this,
                        "Complete your profile before requesting documents.",
                        "Profile Required",
                        JOptionPane.WARNING_MESSAGE);

                    new residentProfile().setVisible(true);
                    this.dispose();
                    return;
                }
            }

            rs.close();
            pst.close();
            conn.close();

            new requestDocument().setVisible(true);
            this.dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_requestPanelMouseClicked

